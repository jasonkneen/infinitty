// Terminal configuration - themes and fonts

export interface TerminalTheme {
  id: string
  name: string
  background: string
  foreground: string
  cursor: string
  cursorAccent: string
  selectionBackground: string
  black: string
  red: string
  green: string
  yellow: string
  blue: string
  magenta: string
  cyan: string
  white: string
  brightBlack: string
  brightRed: string
  brightGreen: string
  brightYellow: string
  brightBlue: string
  brightMagenta: string
  brightCyan: string
  brightWhite: string
}

export interface TerminalFont {
  id: string
  name: string
  family: string
  isMonospace: boolean
  category: 'modern' | 'retro' | 'classic' | 'pixel'
}

export interface UIFont {
  id: string
  name: string
  family: string
  category: 'system' | 'sans' | 'serif' | 'display'
}

export interface SyntaxTheme {
  id: string
  name: string
  category: 'dark' | 'light'
}

