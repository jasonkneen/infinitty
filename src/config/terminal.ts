// Helper to convert theme to xterm.js format
export function themeToXterm(theme: TerminalTheme) {
  return {
    background: theme.background,
    foreground: theme.foreground,
    cursor: theme.cursor,
    cursorAccent: theme.cursorAccent,
    selectionBackground: theme.selectionBackground,
    black: theme.black,
    red: theme.red,
    green: theme.green,
    yellow: theme.yellow,
    blue: theme.blue,
    magenta: theme.magenta,
    cyan: theme.cyan,
    white: theme.white,
    brightBlack: theme.brightBlack,
    brightRed: theme.brightRed,
    brightGreen: theme.brightGreen,
    brightYellow: theme.brightYellow,
    brightBlue: theme.brightBlue,
    brightMagenta: theme.brightMagenta,
    brightCyan: theme.brightCyan,
    brightWhite: theme.brightWhite,
  }
}

// Helper to get subtle border color (20% opacity of white)
export function getSubtleBorder(theme: TerminalTheme): string {
  return `${theme.white}33`
}

// Helper to get hover border color (40% opacity of white)
export function getHoverBorder(theme: TerminalTheme): string {
  return `${theme.white}66`
}
